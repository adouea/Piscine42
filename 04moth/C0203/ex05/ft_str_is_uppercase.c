int	ft_str_is_uppercase(char *str)
{
	int i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0' && str[i] > 64 && str[i] < 91)
		i++;
	if (str[i] == '\0')
		return (1);
	else
		return (0);
}
